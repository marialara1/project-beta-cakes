        <h2 class="info-title paddings marginauto">Política de cookies</h2>

        <h4>¿Qué es una cookie o galleta informática?</h4>
        <p class="info-p"> 
        Son pequeños ficheros de datos que el servidor de Internet envía al ordenador del usuario al acceder a determinadas páginas web y que se almacenan en la memoria del mismo. Las cookies permiten, entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación de un usuario o de su equipo, ver qué páginas se visitan, el tiempo de conexión, analizar el funcionamiento de una página, etc. Las cookies no pueden dañar tu dispositivo y además son muy útiles, ya que nos ayudan a identificar y resolver errores.

        </p>

        <h4>¿Qué tipos de cookies existen?</h4>
        <p class="info-p"> 
        Según la finalidad para la que se traten los datos obtenidos a través de las cookies, podemos distinguir entre:
        <br> <br> - Cookies técnicas: Son aquéllas que permiten al usuario la navegación a través de una página web, plataforma o aplicación y la utilización de las diferentes opciones o servicios que en ella existan como, por ejemplo, controlar el tráfico y la comunicación de datos, identificar la sesión, acceder a partes de acceso restringido, recordar los elementos que integran un pedido, realizar el proceso de compra de un pedido, realizar la solicitud de inscripción o participación en un evento, utilizar elementos de seguridad durante la navegación, almacenar contenidos para la difusión de videos o sonido o compartir contenidos a través de redes sociales.
        </p>

        <h4>Cómo gestionar las cookies</h4>
        <p class="info-p"> 
        Algunas cookies son totalmente necesarias para permitirle utilizar el sitio web y sus funciones, como guardar productos en la cesta de la compra. Sin esas cookies no podríamos facilitarle esas funciones, lo que afectaría al funcionamiento del sitio web y le impediría comprar en él.

        <br> <br>Puede seleccionar qué cookies quiere que guarde en su equipo este sitio web cambiando la configuración de su navegador. Cada navegador ofrece una manera distinta de restringir o bloquear las cookies.

        <br> <br>En el sitio web de la Oficina del Comisionado de Información del Reino Unido (ICO, por sus siglas en inglés), puede encontrar más información sobre cómo configurar cada navegador (tenga en cuenta que no somos responsables de los contenidos de sitios web externos). Para ello, visite https://ico.org.uk/your-data-matters/online/cookies/.

            <br> <br>En la mayoría de los navegadores puede utilizar el modo de navegación privada, en el que normalmente no se guardan datos locales relacionados con la sesión, como las cookies, que suelen eliminarse cuando cierra todas las ventanas.

            <br> <br>Puede instalar un complemento en el navegador para impedir que todos los sitios web que visite con el navegador utilicen Google Analytics (tenga en cuenta que no somos responsables de los contenidos de sitios web externos). Para ello, visite https://tools.google.com/dlpage/gaoptout.


            <h4>Tipos de cookies</h4>

- Cookies de origen: son las que instala el sitio web al que accede. Suelen utilizarse para recordar sus preferencias en el sitio web, como la divisa elegida o los productos de la cesta de la compra.

<br> <br>- Cookies de terceros: son las que instalan sitios web distintos del que está visitando.

<br> <br>- Cookies de sesión: son las cookies temporales que se eliminan cuando cierra el navegador.

<br> <br>- Cookies permanentes: estas cookies se quedan instaladas en su equipo hasta que las elimina o caducan.


